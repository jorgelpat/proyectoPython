def ingreso_camper_hora1():
    id = input("Indique id del camper que desea agregar:\n")
    infArea1={
        "Campers": [],
        "Trainer": "",
        "Area": "1"
    }
    if len(infArea1["Campers"])<33:
        infArea1["Campers"].append(id)
    else:
        print("Ya hay un maximo de 33 campers")
    return "Campers Agregados"

